    private void UPDATEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UPDATEActionPerformed
        // TODO add your handling code here:
        
        try
        {
        stmt = con.createStatement();
        
        int sid = Integer.parseInt(ID.getText());
        String sname = NAME.getText();
        String spos = POSSITION.getText();
        String datestart = (DATESTART.getText());
        String dateended = (DATEENDED.getText());
        int scontact = Integer.parseInt(CONTACT.getText());
        String saddress = (ADDRESS.getText());
        String email = (EMAIL.getText());
        
        String query = "UPDATE staff SET NAME = ?, POSSITTION = ?, DATESTART = ?, DATEENDED = ?, CONTACT = ?, ADDRESS = ?, EMail = ? WHERE ID = ? ";
        PreparedStatement PreparedStmt = con.prepareStatement(query);
        
        PreparedStmt.setString(1, sname);
        PreparedStmt.setString(2, spos);
        PreparedStmt.setString(3, datestart);
        PreparedStmt.setString(4, dateended);
        PreparedStmt.setInt(5, scontact);
        PreparedStmt.setString(6,saddress );
        PreparedStmt.setString(7,email );
        PreparedStmt.setInt(8, sid);
        
        PreparedStmt.executeUpdate();
        JOptionPane.showMessageDialog(null, "updated");
        
        
        }
        catch(Exception e)
        {
        System.out.println(e);
        }
    }//GEN-LAST:event_UPDATEActionPerformed

